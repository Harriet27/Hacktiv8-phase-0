function hitungHuruf(kata) {

  var arrKata = [[null, null]];

  for (var i = 0; i < kata.length; i++) {
    var inspector = kata[i];
    var positionArrKata = 0;
    if (inspector === " ") {
      arrKata.push([null, null]);
      positionArrKata ++

    }




  }

return arrKata;

}

